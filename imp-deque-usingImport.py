from collections import deque
d=deque()
d.append(10)
d.append(20)
d.append(30)
d.appendleft(40)
print(d)
print(d.pop())
print(d.popleft())
print(d)
d.insert(2,10)
print(d)
d.extendleft([15,25])
print(d)
d.rotate(2)
print(d)
print(d.count(10))
d.rotate(-2)
print(d)
d.reverse()
print(d)
print(d[2])
d[2]=100
print(d)
print(d[-1])
print(d[0])
